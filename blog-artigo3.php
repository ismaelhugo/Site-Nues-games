                        <h2>Storytelling e Jogos Digitais.</h2>
                        <div class="divider-1 wow fadeInUp"><span></span></div>
                        <h4><i>Publicado dia 9 de Outubro de 2019 por Nathan Munis</i></h4>
                        <h5><a href="dicionario.php" target="_blank">Dicionário de termos</a></h5>
                        <p align="justify"  style="color: black" >
						
						&emsp;&emsp;&emsp;&emsp;Uma boa história e narrativa, seja em filmes, livros, teatro, quadrinhos é crucial para que essas obras atinjam o seu máximo potencial. Com a evolução da tecnologia é inegável que os gráficos, e possibilidades que um jogo pode fazer se tornaram infinitas, mas não só isso, os vídeos games se tornaram verdadeiras produções artísticas, dignas de cinema. Como qualquer forma de arte, os games querem, e precisam transmitir as emoções e sentimentos certo, no momento exato para cativar o seu expectador, neste caso o jogador. Nada melhor do que utilizar as técnicas de storytelling para atingir esse objetivo. <br><br>
						
						&emsp;&emsp;&emsp;&emsp;Storytelling é arte de contar, desenvolver e adaptar histórias utilizando elementos específicos, personagem, ambiente, conflito e uma mensagem. Esses eventos são divididos em três grandes macros, Introdução,  desenvolvimento e tudo isso culminando para o evento mais esperado, o final. Tendo como principal intuito transmitir uma mensagem de forma inesquecível ao se conectar-se com o jogador em um nível emocional. Cabe ressaltar que existe uma diferença entre a story(historia) e o storytelling(“Narrativa”). O autor até pode ter uma grande historia e personagem bem elaborados,mas por falta de domínio do storytelling conseguir falhar miseravelmente na hora de contá-la.<br><br>						
						
						&emsp;&emsp;&emsp;&emsp;O storytelling antigamente tinha um papel secundário no mundo dos games, servia apenas como uma forma de justificar a gameplay. Devido isso, em sua grande maioria eram narrativas muito simplicista e pouco cativantes. A storytelling surge nos mundos dos games, principalmente pra auxiliar em um conceito de level design chamado de “fluxo cognitivo”. Resume-se em tentar achar um equilíbrio entre impulsionar o jogador a continuar jogando e ansiedade. Esse equilíbrio é importante para quem desenvolve a mecânica e o design de fases de um jogo, fazendo com que o jogador mantenha-se sempre satisfeito e com vontade de continuar jogando.<br><br>						
						
						&emsp;&emsp;&emsp;&emsp;Isso não é apenas importante para o jogo, mas também para a história. Da mesma forma como um escritor quer fazer seu leitor continuar lendo, imaginando, interagindo e desvendando a história que está contando, um jogo quer fazer com que o jogador interage e vivencie a aventura dentro do mundo do jogo, de seus cenários, de sua ambientação, e para que comece e termine a aventura, é importante que o jogador esteja interessado do inicio até o final.   <br><br>						
						
						&emsp;&emsp;&emsp;&emsp;Quando se relaciona com um jogo o principal objetivo e interatividade, fazer com que o jogador faças sua escolhas e não só isso, fazer com que o jogador enxergue o mundo pelo olhos do personagem. E a história é o motor de um jogo, é onde ela vai acontecer, definindo o mundo do jogo e a experiência que o jogador tera durante a jornada. A história e a maneira de contá-la orienta o detalhamento minucioso do jogo, com todos os elementos devem fortalecer a imersão nesse mundo <br><br>						

						&emsp;&emsp;&emsp;&emsp;Umas das técnicas de storytelling de sucesso é a Jornada do Herói, Joseense Campbellita, na obra O Herói de Mil Faces. Posteriormente, adaptada por Christopher Vogler. Sintetizam A jornada do herói em 12 etapas: O mundo Comum; O chamado de aventura; recusa do chamado; encontro com o mentor; a travessia do primeiro limiar, provas, aliados e inimigos; a aproximação da caverna secreta; aprovação; a recompensa; o caminho de volta; a ressurreição e o retorno do elixir<br><br>						
						
						
						
						<img src="assets/img/galeria/storytelling.png" width=1200 height=900><br><br>
						
						
						&emsp;&emsp;&emsp;&emsp;Podemos identificar esse tipo de narrativa em quase todos jogos eletrônicos, e aplicá-los a esse “Story Spine”, Super Mario pode ser usado como exemplo disso. Mario é obrigado a sair do seu mundo comum para enfrentar uma aventura atrás da princesa, ele atravessa o primeiro limiar do mundo cotidiano, ele passa por provações, que nesse caso seriam as fazes do jogo, ele faz aliados e conhecem os inimigos, passa por outras provações e no final ele derrota o Bowser e descobre que a princesa esta em outro castelo.<br><br>						
						
						&emsp;&emsp;&emsp;&emsp;Outro exemplo de sucesso, é como a pixel conta suas histórias, divididas em Introdução, desenvolvimento, e final. Para construir essa estrutura o contador da histórias deve conseguir utilizando alguns “Story Beats”,mostrando os momentos mais importantes da sua história mostrando o que está acontecendo, não como. Esses “Story Beats” são:<br><br>						
						
						&emsp;&emsp;&emsp;&emsp;– Era uma vez… <br>						
						&emsp;&emsp;&emsp;&emsp;– Todo dia… <br>						
						&emsp;&emsp;&emsp;&emsp;– Até que um dia… <br>						
						&emsp;&emsp;&emsp;&emsp;– Por causa disso… <br>						
						&emsp;&emsp;&emsp;&emsp;– Por isso… <br>						
						&emsp;&emsp;&emsp;&emsp;– E por isso… <br>
						&emsp;&emsp;&emsp;&emsp;– Até que finalmente… <br>							
						&emsp;&emsp;&emsp;&emsp;– Desde então, toda vez que… <br>	
						&emsp;&emsp;&emsp;&emsp;– A moral da história é…<br>							
						&emsp;&emsp;&emsp;&emsp;Podemos notar que a maneira que uma história é contada é cruacial para o sucesso ou fiasco de um jogo. Nos dias atuais, onde as inovações gráficas não surpreende mais tantos os fãs, a industria de games precisa achar uma nova forma de cativar seus clientes. Por isso, podemos notar cada vez mais as desenvolvedoras investindo em um bom storytelling. Exemplos disso são as franquias de Assassin’s Creed, God of War, Metal Gear, Resident Evil e GTA.<br><br>

