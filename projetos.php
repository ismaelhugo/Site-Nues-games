                        <h3>Definição de Processos de Construção de Jogos Digitais com Suporte na Engenharia de Software</h3>
                        <br><br>
                        <h4 style="color: black;">RESUMO</h4>
                        <p align="justify"  style="color: black; ">
						<font size="4" face="Calibri">
							O presente projeto tem como objetivo permitir que alunos da graduação do curso de Sistemas de Informação e demais envolvidos tenham capacitação para entenderem como a ES está inserida em diversos contextos de produtos e principalmente na definição de processos de construção de jogos digitais e não apenas em sistemas corporativos. A área de games, diversa e atrativa que é, se torna um campo importante para a inserção de jovens em boas práticas de ES. Com isso, é importante fomentar o uso dessas práticas e buscar sua aplicação em um campo ainda pouco explorado, nacional e internacionalmente, pelas potencialidades que proveem os recursos da ES.<br>
							Para dar sustentação ao objetivo central dessa proposta, seus objetivos específicos estão voltados a introduzir aos participantes a um conjunto amplo de ações que visam criar um arcabouço sustentável e em diversas frentes, como: potencializar o aprendizado, incitar à criatividade, potencializar a prática de ES, fomentar o pensamento organizado e metódico, trabalhar em equipe, transmitir conhecimento para atingir alto número de interessados, usar ferramentas de software, despertar o interesse em transmitir o conhecimento, treinar a comunicação em público e orquestrar ações multidisciplinares.  <br>
							Essas ações e frentes de trabalho serão organizadas de forma a atingir os seguintes objetivos específicos. <br>
							i. Estabelecer e organizar o desenvolvimento de games em duas classificações: Serious Games (jogos sérios) ou Entertainment (jogos de entretenimento). <br>
							ii. Estabelecer e sistematizar processos que visem à concepção, modelagem, prototipação, validação de games;<br>
							iii. Estabelecer e sistematizar processos para construção de games que proporcionem a sua codificação, arquitetura e gestão de configuração; <br>
							Para os participantes, espera-se que os conceitos aprendidos sejam valiosos para a sua formação profissional e humana para que possam atuar capacitados no mercado de trabalho e com visão prática do uso da ES para aplicarem em diversas áreas de conhecimento.<br><br>

