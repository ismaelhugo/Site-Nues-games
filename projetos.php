                        <h3>PROGRAMA DE EXTENSÃO - Engenharia de Software e Jogos Digitais</h3>
                        <br><br>
                        <h4 style="color: black;">RESUMO</h4>
                        <p align="justify"  style="color: black" >
                           O desenvolvimento de jogos digitais (games), Serious Games (jogos sérios) ou Entertainment (jogos de entretenimento), considerados aqui tais produtos como software, necessita o envolvimento de atores de perfis distintos em seu processo de construção. Além de tratar de um processo produtivo onde a criatividade é um dos fatores inerentes ao processo, também exige a atenção da Engenharia de Software (ES), uma vez que há um nível alto grau de complexidade da lógica de funcionamento, alta variabilidade de requisitos e distintos objetivos a serem atingidos. Nesse contexto, se faz necessário um estudo criterioso de procedimentos, ferramentas, técnicas de modelagem e processos que possam dar suporte à garantia de qualidade do game (produto de software) a ser desenvolvido, bem como seu processo de produção. A presente proposta objetiva possibilitar a alunos e demais envolvidos, internos e externos, participar do processo produtivo de games associado às boas práticas de ES. Tal documento visa formalizar a criação de um programa de extensão no Departamento de Computação (DECOMP), que servirá como estrutura ampla e de longo prazo para diversos projetos relacionados ao contexto de ES aplicada ao desenvolvimento de games, e também iniciativas que objetivem sua socialização, ampla divulgação para aprendizado, captação de recursos, envolvimento de alunos e docentes, parcerias interinstitucional e multilateral, entre outras que possam viabilizar e sustentar o programa.<br>
                           <b>Palavras-chave:</b> software; engenharia; game <br>

                            <b>Projeto financiado com apoio:</b> Iniciação Tecnológica, IT 2019, FAPERJ. <br><br>
                        </p>

                        <h3>Definição de Processos de Construção de Jogos Digitais com Suporte na Engenharia de Software</h3>
                        <br><br>
                        <h4 style="color: black;">RESUMO</h4>
                        <p align="justify"  style="color: black" >
                            O desenvolvimento de jogos digitais (games), Serious Games (jogos sérios) ou Entertainment (jogos de entretenimento), considerados aqui tais produtos como software, necessita o envolvimento de atores de perfis distintos em seu processo de construção. Além de tratar de um processo produtivo onde a criatividade é um dos fatores inerentes ao processo, também exige a atenção da Engenharia de Software (ES), uma vez que há um nível alto grau de complexidade da lógica de funcionamento, alta variabilidade de requisitos e distintos objetivos a serem atingidos. Nesse contexto, se faz necessário um estudo criterioso de procedimentos, ferramentas, técnicas de modelagem e processos que possam dar suporte à garantia de qualidade do game (produto de software) a ser desenvolvido, bem como seu processo de produção. A presente proposta objetiva definir processos de construção de jogos digitais com suporte na ES com base em critérios técnicos e formalmente definidos para possibilitar a alunos e demais envolvidos, internos e externos, participar do processo produtivo de games associado às boas práticas de ES. O projeto de pesquisa será correlacionado ao Programa de Extensão “Engenharia de Software e Jogos Digitais” aprovado no DECOMP/ICE e formalizado junto à PROEXT sob número de processo 23083.011415/2019-50. O projeto de pesquisa ainda não tem fontes de recursos externos a não ser a bolsa aqui solicitada, porém o Programa de Extensão já conta com uma bolsa de apoio tecnológico financiada pela FAPERJ.
                           <br>
